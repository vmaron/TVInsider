package com.vmaron.tvinsider.Model;

public class TvShow
{
    private float id;
    private String url;
    private String name;


    // Getter Methods

    public float getId()
    {
        return id;
    }

    public String getUrl()
    {
        return url;
    }

    public String getName()
    {
        return name;
    }


    // Setter Methods

    public void setId(float id)
    {
        this.id = id;
    }

    public void setUrl(String url)
    {
        this.url = url;
    }

    public void setName(String name)
    {
        this.name = name;
    }
}





